// utils.go - Utility functions
package main

import "math/rand"

// randomString generates random VM names like "VM-a1b2"
func randomString(length int) string {
	const charset = "abcdefghijklmnopqrstuvwxyz0123456789"
	b := make([]byte, length)
	for i := range b {
		b[i] = charset[rand.Intn(len(charset))]
	}
	return string(b)
}
